package controllers;

/**
 *
 * @author M.Ridvan Ozcan
 */
public class Note {

    private String Name;
    private String Note;
    private String Info;

    public Note(String Name, String Note, String Info) {
        this.Name = Name;
        this.Note = Note;
        this.Info = Info;
    }



    /**
     * @return the Name
     */
    public String getName() {
        return Name;
    }

    /**
     * @param Name the Name to set
     */
    public void setName(String Name) {
        this.Name = Name;
    }

    /**
     * @return the Note
     */
    public String getNote() {
        return Note;
    }

    /**
     * @param Note the Note to set
     */
    public void setNote(String Note) {
        this.Note = Note;
    }

    /**
     * @return the Info
     */
    public String getInfo() {
        return Info;
    }

    /**
     * @param Info the Info to set
     */
    public void setInfo(String Info) {
        this.Info = Info;
    }

}
